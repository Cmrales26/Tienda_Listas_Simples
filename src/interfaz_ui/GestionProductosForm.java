    {//GEN-HEADEREND:event_btnCancelarActionPerformed
        limpiarCampos();
        configuracion(1);
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void configuracion(int caso)
    {
        switch (caso) {
            case 1:
                btnActualizar.setEnabled(false);
                btnCancelar.setEnabled(false);
                btnGuardar.setEnabled(true);
                btnLimpiar.setEnabled(true);
                btnLimpiar.setEnabled(true);
                break;
            case 2:
                btnActualizar.setEnabled(true);
                btnCancelar.setEnabled(true);
                btnGuardar.setEnabled(false);
                btnLimpiar.setEnabled(false);
                txtCodigo.setEnabled(false);
                break;
        }

    }

    private void limpiarCampos()
    {
        txtCodigo.setText("");
        tXtNombre.setText("");
        txtPrecio.setText("");
    }

    @SuppressWarnings("empty-statement")
    private void crearTabla()
    {
        modelo.getDataVector().removeAllElements();;
        tblProductos.updateUI();
        if (pilaProducto.cantidadNodos() > 0) {
            NodoProducto nodo = pilaProducto.getNodoRaizProducto();
            while (nodo != null) {
                Producto producto = nodo.valorNodo;
                Vector vector = new Vector();
                vector.add(producto.getCodigo());
                vector.add(producto.getNombre());
                vector.add(producto.getPrecio());
                modelo.addRow(vector);
                nodo = nodo.siguienteNodo;
            }
        }
    }

