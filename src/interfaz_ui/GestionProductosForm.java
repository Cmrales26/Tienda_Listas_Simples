    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnGuardarActionPerformed
    {//GEN-HEADEREND:event_btnGuardarActionPerformed
        try {
            if (!txtCodigo.getText().isEmpty() && !tXtNombre.getText().isEmpty() && !txtPrecio.getText().isEmpty()) {
                Producto nuevoProducto = new Producto(txtCodigo.getText().trim(), tXtNombre.getText().trim(), txtPrecio.getText().trim());
                boolean agregado = pilaProducto.insertarProducto(nuevoProducto);
                if (agregado) {
                    JOptionPane.showMessageDialog(this, "¡El producto se agregó exitosamente!");
                    limpiarCampos();
                    crearTabla();
                    vista_principal.pilaProducto.recargarRaiz();
                    vista_principal.listaProductos();
                } else {
                    JOptionPane.showMessageDialog(this, "¡El el código del producto ya existe!.");
                }
            }
        } catch (HeadlessException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

