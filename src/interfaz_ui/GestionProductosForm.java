    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnActualizarActionPerformed
    {//GEN-HEADEREND:event_btnActualizarActionPerformed
        if (!txtCodigo.getText().isEmpty() && !tXtNombre.getText().isEmpty() && !txtPrecio.getText().isEmpty()) {
            Producto productoEditado = new Producto(txtCodigo.getText().trim(), tXtNombre.getText().trim(), txtPrecio.getText().trim());
            boolean edicion = pilaProducto.editarProducto(productoEditado);
            if (edicion) {
                JOptionPane.showMessageDialog(this, "¡El producto se actualizó exitosamente!");
                limpiarCampos();
                configuracion(1);
                crearTabla();
                vista_principal.pilaProducto.recargarRaiz();
                vista_principal.listaProductos();
            } else {
                JOptionPane.showMessageDialog(this, "¡El el código del producto ya existe!.");
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_tblProductosMouseClicked
    {//GEN-HEADEREND:event_tblProductosMouseClicked
        int fila = tblProductos.getSelectedRow();
        if (fila != -1) {
            configuracion(2);
            txtCodigo.setText(modelo.getValueAt(fila, 0).toString());
            tXtNombre.setText(modelo.getValueAt(fila, 1).toString());
            txtPrecio.setText(modelo.getValueAt(fila, 2).toString());
        }
    }//GEN-LAST:event_tblProductosMouseClicked

